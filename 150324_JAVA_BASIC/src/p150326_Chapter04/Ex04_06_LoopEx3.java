public class Ex04_06_LoopEx3 {

	public static void main(String[] args) {
			//		중첩반복문 : 구구단 작성하기
		for(int i=2; i<=9; i++)	{
			for(int j=1; j<=9; j++)
				System.out.print( i + "*"+ j + "=" + (i*j) +"\t");
			System.out.println();
		}
		System.out.println("-----구구단 가로-----");
			//		중첩반복문 : 구구단 작성하기
		for(int j=1; j<=9; j++)	{
			for(int i=2; i<=9; i++)
				System.out.print( i + "*"+ j + "=" + (i*j) +"\t");
			System.out.println();
		}
		System.out.println("-----구구단 세로-----");
				// 두개의 주사위를 던졌을 때, 눈의 합이 6이 되는 모든 경우의 수를 
				//출력하는 프로그램을 작성하시오.
		for(int i=1;i<=6; i++)
			for(int j=1; j<=6; j++)
				if((i+j)==6)
					System.out.print(i+"+"+j+"\t\t");
		System.out.println("-----주사위-----");
		
		// 방정식 2x+4y=10의 모든 해를 구하시오.
		// 단, x와 y는 정수이고 각각의 범위는 0<=x<=10, 0<=y<=10이다
		for(int x=0; x<10; x++)
			for(int y=0; y<=10; y++)
				if((2*x+4*y)==10)System.out.print("x:"+x+",y:"+y+"\t\t");
		System.out.println("----- 방정식 -----");
	}
}
